/** Whether the active UI language is one of the Chinese variants. Several
 *  places used to test `lang === "zh"` to swap typography (tighter
 *  letter-spacing, larger gap between category tabs). Centralize that
 *  test so adding zh-Hant didn't require touching every caller. */
export function isChinese(lang: Lang): boolean {
  return lang === "zh-Hans" || lang === "zh-Hant";
}

/** Whether the active UI language uses East Asian (CJK) script. The
 *  letter-spacing tweaks that helped Chinese also apply to Japanese and
 *  Korean — wide glyphs don't need extra tracking the way Latin text
 *  does. Use this for typography decisions; use `isChinese` only when
 *  the logic is specifically about Chinese (e.g., zh-CN locale fallback). */
export function isCJK(lang: Lang): boolean {
  return isChinese(lang) || lang === "ja" || lang === "ko";
}

/** React hook — returns `{ t, lang, setLang }` and re-renders on language change. */
export function useTranslation() {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    setLangState(currentLang);
    // Apply <html lang> on first mount so screen readers and Intl APIs
    // see the stored language preference. We deliberately do NOT touch
    // `dir` — see setLang() for why we keep the page LTR even for Farsi.
    const meta = languageMeta(currentLang);
    try {
      document.documentElement.setAttribute("lang", meta.locale);
    } catch {
      /* ignore */
    }

    function onChange(e: Event) {
      const detail = (e as CustomEvent<Lang>).detail;
      if (isLang(detail)) setLangState(detail);
    }
    window.addEventListener(EVENT_NAME, onChange);
    return () => window.removeEventListener(EVENT_NAME, onChange);
  }, []);

  return {
    lang,
    t: (key: TranslationKey | string) => t(key, lang),
    setLang: (l: Lang) => setLang(l),
  };
}
