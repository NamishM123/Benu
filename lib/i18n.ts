// Supported UI languages. Codes follow BCP 47 — `zh-Hans` for Simplified
// Chinese (Mandarin), `zh-Hant` for Traditional (the script Cantonese
// readers in CA/HK/TW expect). The legacy "zh" value that lives in older
// localStorage entries is migrated to "zh-Hans" on load.
export type Lang =
  | "en"
  | "es"
  | "zh-Hans"
  | "zh-Hant"
  | "tl"
  | "vi"
  | "ko"
  | "fa"
  | "hy"
  | "ru"
  | "ja";

export const LANGUAGES: ReadonlyArray<{
  code: Lang;
  label: string; // shown in the switcher, in the language's own script
  dir: "ltr" | "rtl";
  locale: string; // BCP 47 for Intl.* APIs (date/number formatting)
}> = [
  { code: "en", label: "English", dir: "ltr", locale: "en-US" },
  { code: "es", label: "Español", dir: "ltr", locale: "es-MX" },
  { code: "zh-Hans", label: "简体中文", dir: "ltr", locale: "zh-CN" },
  { code: "zh-Hant", label: "繁體中文", dir: "ltr", locale: "zh-TW" },
  { code: "tl", label: "Filipino", dir: "ltr", locale: "fil-PH" },
  { code: "vi", label: "Tiếng Việt", dir: "ltr", locale: "vi-VN" },
  { code: "ko", label: "한국어", dir: "ltr", locale: "ko-KR" },
  { code: "ja", label: "日本語", dir: "ltr", locale: "ja-JP" },
  { code: "fa", label: "فارسی", dir: "rtl", locale: "fa-IR" },
  { code: "hy", label: "Հայերեն", dir: "ltr", locale: "hy-AM" },
  { code: "ru", label: "Русский", dir: "ltr", locale: "ru-RU" },
];

export const SUPPORTED_LANGS = LANGUAGES.map((l) => l.code) as readonly Lang[];

export function isLang(s: unknown): s is Lang {
  return typeof s === "string" && (SUPPORTED_LANGS as readonly string[]).includes(s);
}

export function languageMeta(lang: Lang) {
  return LANGUAGES.find((l) => l.code === lang) ?? LANGUAGES[0];
}

